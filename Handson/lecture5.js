
/*....................Arithmetic Operations.......................*/


// (var = function-scoped, can be redeclared or reassigned)
var num1 = 24;
var num2 = 3;
var result;


// Addition
result = num1 + num2;      // adds 24 + 3
console.log("Addition: " + result); //  27


// Subtraction
result = num1 - num2;      // subtracts 24 - 3
console.log("Subtraction: " + result); // 21


// Multiplication
result = num1 * num2;      // multiplies 24 *3
console.log("Multiplication: " + result); //  72


// Division
result = num1 / num2;      // divides 24 รท 3
console.log("Division: " + result); // 8


// Modulo (Remainder)
result = num1 % num2;      // remainder of 24 รท 3
console.log("Modulo: " + result); //  0
